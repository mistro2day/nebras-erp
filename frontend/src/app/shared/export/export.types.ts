/**
 * أنواع نظام التصدير الموحّد لمشروع نبراس (CSV / Excel / PDF / طباعة).
 * تُستخدم في جميع الوحدات لضمان تناسق المخرجات ودعم العربية (RTL).
 */
export interface ExportColumn {
  /** مفتاح الحقل في كائن الصف. */
  key: string;
  /** العنوان الظاهر في رأس الجدول. */
  label: string;
  /** محوّل قيمة اختياري لعرض مخصّص (مثال: تحويل رمز حالة إلى نص عربي). */
  map?: (row: any) => string | number;
  /** محاذاة القيمة: بداية (نص) أو نهاية (أرقام). */
  align?: 'start' | 'end';
  /** عرض العمود في ملف Excel (بالوحدات). */
  width?: number;
}

export interface ExportMeta {
  /** عنوان المستند (يظهر في الترويسة واسم الملف). */
  title: string;
  /** وصف فرعي اختياري أسفل العنوان. */
  subtitle?: string;
  /** اسم الملف بدون امتداد (افتراضي: العنوان). */
  filename?: string;
}
